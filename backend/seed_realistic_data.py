def attendance_status(student_no, schedule_seq):
    """Повертає 'present' | 'late' | 'absent' детерміновано."""
    r = _rng("att", student_no, schedule_seq).random()
    if r < 0.80:
        return "present"
    if r < 0.92:
        return "late"
    return "absent"


