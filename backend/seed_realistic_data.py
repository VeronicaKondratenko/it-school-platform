def is_submitted(student_no, akey, mode):
    """Чи здав студент завдання (детерміновано)."""
    if mode in ("graded_all", "graded_partial"):
        return True
    if mode == "due_soon":
        return _rng("sub", student_no, akey).random() < 0.6
    return False  # future


def is_graded(student_no, akey, mode):
    """Чи оцінене здане завдання."""
    if mode == "graded_all":
        return True
    if mode == "graded_partial":
        return _rng("grade", student_no, akey).random() < 0.7
    return False


def score_for(student_no, akey):
    r = _rng("score", student_no, akey)
    roll = r.random()
    if roll < 0.15:
        return r.randint(55, 69)
    if roll < 0.55:
        return r.randint(70, 84)
    return r.randint(85, 100)


